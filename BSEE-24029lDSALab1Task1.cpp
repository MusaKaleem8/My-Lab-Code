#include <iostream>
using namespace std;
int main()
{
    // For-loop
    cout << "For-loop Example:" << endl;
    for (int i = 1; i <= 5; i++) {
        cout << "i = " << i << endl;
    }

    // While-loop
    cout << "While-loop Example:" << endl;
    int j = 1;
    while (j <= 5) {
        cout << "j = " << j << endl;
        j++;
    }

}
